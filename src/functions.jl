function compute_probs(dist::MultistageTrueErrorModel{T}) where {T}
    (; b, cb, cf, ϵ) = dist
    ϵ₁, ϵ₂, ϵ₃, ϵ₄ = ϵ
    θ = zeros(T, 16)

    # 1 1 1 1
    θ[1] =
        b * (1 - ϵ₁) * (1 - ϵ₂) * cb * (1 - ϵ₃) * (1 - ϵ₄) +
        b * (1 - ϵ₁) * (1 - ϵ₂) * (1 - cb) * ϵ₃ * ϵ₄ +
        (1 - b) * ϵ₁ * ϵ₂ * cf * ϵ₃ * ϵ₄ +
        (1 - b) * ϵ₁ * ϵ₂ * (1 - cf) * (1 - ϵ₃) * (1 - ϵ₄)

    # 0 1 1 1
    θ[2] =
        b * ϵ₁ * (1 - ϵ₂) * cb * (1 - ϵ₃) * (1 - ϵ₄) +
        b * ϵ₁ * (1 - ϵ₂) * (1 - cb) * ϵ₃ * ϵ₄ +
        (1 - b) * (1 - ϵ₁) * ϵ₂ * cf * ϵ₃ * ϵ₄ +
        (1 - b) * (1 - ϵ₁) * ϵ₂ * (1 - cf) * (1 - ϵ₃) * (1 - ϵ₄)

    # 1 0 1 1
    θ[3] =
        b * (1 - ϵ₁) * ϵ₂ * cb * (1 - ϵ₃) * (1 - ϵ₄) +
        b * (1 - ϵ₁) * ϵ₂ * (1 - cb) * ϵ₃ * ϵ₄ +
        (1 - b) * ϵ₁ * (1 - ϵ₂) * cf * ϵ₃ * ϵ₄ +
        (1 - b) * ϵ₁ * (1 - ϵ₂) * (1 - cf) * (1 - ϵ₃) * (1 - ϵ₄)

    # 0 0 1 1
    θ[4] =
        b * ϵ₁ * ϵ₂ * cb * (1 - ϵ₃) * (1 - ϵ₄) +
        b * ϵ₁ * ϵ₂ * (1 - cb) * ϵ₃ * ϵ₄ +
        (1 - b) * (1 - ϵ₁) * (1 - ϵ₂) * cf * ϵ₃ * ϵ₄ +
        (1 - b) * (1 - ϵ₁) * (1 - ϵ₂) * (1 - cf) * (1 - ϵ₃) * (1 - ϵ₄)

    # 1 1 0 1
    θ[5] =
        b * (1 - ϵ₁) * (1 - ϵ₂) * cb * ϵ₃ * (1 - ϵ₄) +
        b * (1 - ϵ₁) * (1 - ϵ₂) * (1 - cb) * (1 - ϵ₃) * ϵ₄ +
        (1 - b) * ϵ₁ * ϵ₂ * cf * (1 - ϵ₃) * ϵ₄ +
        (1 - b) * ϵ₁ * ϵ₂ * (1 - cf) * ϵ₃ * (1 - ϵ₄)

    # 0 1 0 1
    θ[6] =
        b * ϵ₁ * (1 - ϵ₂) * cb * ϵ₃ * (1 - ϵ₄) +
        b * ϵ₁ * (1 - ϵ₂) * (1 - cb) * (1 - ϵ₃) * ϵ₄ +
        (1 - b) * (1 - ϵ₁) * ϵ₂ * cf * (1 - ϵ₃) * ϵ₄ +
        (1 - b) * (1 - ϵ₁) * ϵ₂ * (1 - cf) * ϵ₃ * (1 - ϵ₄)

    # 1 0 0 1
    θ[7] =
        b * (1 - ϵ₁) * ϵ₂ * cb * ϵ₃ * (1 - ϵ₄) +
        b * (1 - ϵ₁) * ϵ₂ * (1 - cb) * (1 - ϵ₃) * ϵ₄ +
        (1 - b) * ϵ₁ * (1 - ϵ₂) * cf * (1 - ϵ₃) * ϵ₄ +
        (1 - b) * ϵ₁ * (1 - ϵ₂) * (1 - cf) * ϵ₃ * (1 - ϵ₄)

    # 0 0 0 1
    θ[8] =
        b * ϵ₁ * ϵ₂ * cb * ϵ₃ * (1 - ϵ₄) +
        b * ϵ₁ * ϵ₂ * (1 - cb) * (1 - ϵ₃) * ϵ₄ +
        (1 - b) * (1 - ϵ₁) * (1 - ϵ₂) * cf * (1 - ϵ₃) * ϵ₄ +
        (1 - b) * (1 - ϵ₁) * (1 - ϵ₂) * (1 - cf) * ϵ₃ * (1 - ϵ₄)

    # 1 1 1 0
    θ[9] =
        b * (1 - ϵ₁) * (1 - ϵ₂) * cb * (1 - ϵ₃) * ϵ₄ +
        b * (1 - ϵ₁) * (1 - ϵ₂) * (1 - cb) * ϵ₃ * (1 - ϵ₄) +
        (1 - b) * ϵ₁ * ϵ₂ * cf * ϵ₃ * (1 - ϵ₄) +
        (1 - b) * ϵ₁ * ϵ₂ * (1 - cf) * (1 - ϵ₃) * ϵ₄

    # 0 1 1 0
    θ[10] =
        b * ϵ₁ * (1 - ϵ₂) * cb * (1 - ϵ₃) * ϵ₄ +
        b * ϵ₁ * (1 - ϵ₂) * (1 - cb) * ϵ₃ * (1 - ϵ₄) +
        (1 - b) * (1 - ϵ₁) * ϵ₂ * cf * ϵ₃ * (1 - ϵ₄) +
        (1 - b) * (1 - ϵ₁) * ϵ₂ * (1 - cf) * (1 - ϵ₃) * ϵ₄

    # 1 0 1 0
    θ[11] =
        b * (1 - ϵ₁) * ϵ₂ * cb * (1 - ϵ₃) * ϵ₄ +
        b * (1 - ϵ₁) * ϵ₂ * (1 - cb) * ϵ₃ * (1 - ϵ₄) +
        (1 - b) * ϵ₁ * (1 - ϵ₂) * cf * ϵ₃ * (1 - ϵ₄) +
        (1 - b) * ϵ₁ * (1 - ϵ₂) * (1 - cf) * (1 - ϵ₃) * ϵ₄

    # 0 0 1 0
    θ[12] =
        b * ϵ₁ * ϵ₂ * cb * (1 - ϵ₃) * ϵ₄ +
        b * ϵ₁ * ϵ₂ * (1 - cb) * ϵ₃ * (1 - ϵ₄) +
        (1 - b) * (1 - ϵ₁) * (1 - ϵ₂) * cf * ϵ₃ * (1 - ϵ₄) +
        (1 - b) * (1 - ϵ₁) * (1 - ϵ₂) * (1 - cf) * (1 - ϵ₃) * ϵ₄

    # 1 1 0 0
    θ[13] =
        b * (1 - ϵ₁) * (1 - ϵ₂) * cb * ϵ₃ * ϵ₄ +
        b * (1 - ϵ₁) * (1 - ϵ₂) * (1 - cb) * (1 - ϵ₃) * (1 - ϵ₄) +
        (1 - b) * ϵ₁ * ϵ₂ * cf * (1 - ϵ₃) * (1 - ϵ₄) +
        (1 - b) * ϵ₁ * ϵ₂ * (1 - cf) * ϵ₃ * ϵ₄

    # 0 1 0 0
    θ[14] =
        b * ϵ₁ * (1 - ϵ₂) * cb * ϵ₃ * ϵ₄ +
        b * ϵ₁ * (1 - ϵ₂) * (1 - cb) * (1 - ϵ₃) * (1 - ϵ₄) +
        (1 - b) * (1 - ϵ₁) * ϵ₂ * cf * (1 - ϵ₃) * (1 - ϵ₄) +
        (1 - b) * (1 - ϵ₁) * ϵ₂ * (1 - cf) * ϵ₃ * ϵ₄

    # 1 0 0 0
    θ[15] =
        b * (1 - ϵ₁) * ϵ₂ * cb * ϵ₃ * ϵ₄ +
        b * (1 - ϵ₁) * ϵ₂ * (1 - cb) * (1 - ϵ₃) * (1 - ϵ₄) +
        (1 - b) * ϵ₁ * (1 - ϵ₂) * cf * (1 - ϵ₃) * (1 - ϵ₄) +
        (1 - b) * ϵ₁ * (1 - ϵ₂) * (1 - cf) * ϵ₃ * ϵ₄

    # 0 0 0 0
    θ[16] =
        b * ϵ₁ * ϵ₂ * cb * ϵ₃ * ϵ₄ +
        b * ϵ₁ * ϵ₂ * (1 - cb) * (1 - ϵ₃) * (1 - ϵ₄) +
        (1 - b) * (1 - ϵ₁) * (1 - ϵ₂) * cf * (1 - ϵ₃) * (1 - ϵ₄) +
        (1 - b) * (1 - ϵ₁) * (1 - ϵ₂) * (1 - cf) * ϵ₃ * ϵ₄
    return θ
end
